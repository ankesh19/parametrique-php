                    <h1>FTTX – CLASSIFICATION</h1>
                    <p>1. <strong>The FTTH:</strong> Also called the fibre to the home network solution. 
                        Here the optical fibre terminates at the home of the client. 
                        This allows him to experience unmatched data connectivity and speed.
                        In terms of speed and reliability, the FTTH is the most efficient and
                        fastest solution.
                       <br><br>
                       2. <strong>The FTTB:</strong> It is also known as fibre to the building, 
                       where the optical fibre terminates at the basement of a building (this is
                       typically provided in a flat or office where there are multiple users of 
                       the broadband network). From there data is transmitted through a variety of process.
                       <br><br> 
                       3. <strong>The FTTN:</strong>This is also known as fibre to the node rollout.
                        In this type of solution, the optical fibre is provided to some distance away
                        from the customers’ home or office. It ends in a cabinet and the last-minute
                        part of the network from that cabinet point to the home or the office of the
                        client is provided through the copper wire. This type of network does not
                        provide you with the optimum speed due to the inherent weakness of the last
                        mile copper wire connectivity.
                       <br><br> 
                       4. <strong>The FTTC:</strong> This is also called fibre to the curb. In this
                        type of network deployment, the service provider provides optical cable connection
                        within 300 yards of a client’s establishment. Although this provides faster 
                        connectivity than the FTTN, it still has to depend upon the copper wire for 
                        the last mile connectivity.  
                    </p>
